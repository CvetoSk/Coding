function ConcatenateData (input) {
    let FirstName = input[0]
    let LastName = input[1]
    let Age = input[2]
    let Town = input[3]

    console.log(`You are ${FirstName} ${LastName}, a ${Age}-years old person from ${Town}.`);
}

