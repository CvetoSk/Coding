function swit (input){
    let i = (input[0]);
    switch(i) {
    case '1': 
        console.log("monday");
        break;
    case '2': 
        console.log("tus");
        break;
    case '3': 
        console.log("wed");
        break;
    case '4': 
        console.log("thur");
        break;
    case '5': 
        console.log("fry");
        break;
default: 
    console.log("wrong");

    }
}

